public String nombre;
public String razon_social;
public String rfc;
public String rnf;
public String tipo;
public String fecha_alta;

    public String getId_padron_industria() {
        return id_padron_industria;
    }

    public void setId_padron_industria(String id_padron_industria) {
        this.id_padron_industria = id_padron_industria;
    }

